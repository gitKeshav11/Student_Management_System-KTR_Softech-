/// use of Relational operators...........


public class Realtional_Operator {
    public static void main(String[] args){
        int a=10, b=20;
        System.out.println(a==b);  // 10==20  false
        System.out.println(a!=b);  // 10!=20  true
        System.out.println(a>b);   // 10>20   false
        System.out.println(a<b);   // 10<20   true
        System.out.println(a<=b);  // 10<=20  true
        System.out.println(a>=b);  // 10>=20  flase
    }
}
